package io.jrb.labs.commons.security;

import io.jrb.labs.commons.model.UserEntity;
import io.jrb.labs.commons.repository.UserEntityRepository;
import io.jrb.labs.commons.service.user.UserServiceImpl;
import org.springframework.boot.autoconfigure.domain.EntityScan;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.ComponentScan;
import org.springframework.context.annotation.Configuration;
import org.springframework.data.domain.ReactiveAuditorAware;
import org.springframework.data.r2dbc.repository.config.EnableR2dbcRepositories;
import org.springframework.security.core.Authentication;
import org.springframework.security.core.context.ReactiveSecurityContextHolder;
import org.springframework.security.core.context.SecurityContext;
import org.springframework.security.oauth2.jwt.Jwt;

@Configuration
@EntityScan(basePackageClasses = { UserEntity.class })
@EnableR2dbcRepositories(basePackageClasses = { UserEntityRepository.class })
@ComponentScan(basePackageClasses = { UserServiceImpl.class })
public class UserSecurityJavaConfig {

    @Bean
    public ReactiveAuditorAware<String> auditorAware() {
        return () -> ReactiveSecurityContextHolder.getContext()
                .map(SecurityContext::getAuthentication)
                .filter(Authentication::isAuthenticated)
                .map(Authentication::getPrincipal)
                .map(Jwt.class::cast)
                .map(Jwt::getSubject);
    }

}
